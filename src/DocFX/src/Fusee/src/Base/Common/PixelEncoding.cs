namespace Fusee.Base.Common
{
    /// <summary>
    /// Enum describing the encoding of a pixel.
    /// </summary>
    public enum PixelEncoding
    {
        /// <summary>
        /// Uint = 0-255 representation of a pixel (channel)
        /// </summary>
        Uint,
        /// <summary>
        /// Float = 0.0f-1.0f representation of a pixel (channel)
        /// </summary>
        Float
    }
}